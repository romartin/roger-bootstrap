package org.roger600.uberfire.testapp.model.api.impl;

public class AnotherValue {
    
    private final String id;
    private final String value;

    public AnotherValue(String id, String value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public String getValue() {
        return value;
    }
}
